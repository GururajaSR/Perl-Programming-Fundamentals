#!/usr/bin/perl
use strict;
use warnings;

# Declaring scalar variables
my $name = "Alice";
my $age = 25;

# Printing scalar variables
print "Name: $name\n";
print "Age: $age\n";

# Declaring array variables
my @colors = ("red", "green", "blue");

# Printing array variables
print "Colors: @colors\n";

# Declaring hash variables
my %fruit_colors = ("apple" => "red", "banana" => "yellow", "grape" => "purple");

# Printing hash variables
print "Apple is $fruit_colors{'apple'}\n";

